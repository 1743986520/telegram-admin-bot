# 明確列出所有零寬字元 Unicode 碼位
_ZERO_WIDTH = {
    "​",  # Zero Width Space
    "‌",  # Zero Width Non-Joiner
    "‍",  # Zero Width Joiner
    "‎",  # Left-to-Right Mark
    "‏",  # Right-to-Left Mark
    "⁠",  # Word Joiner
    "⁡",  # Function Application
    "⁢",  # Invisible Times
    "⁣",  # Invisible Separator
    "⁤",  # Invisible Plus
    "﻿",  # Zero Width No-Break Space (BOM)
    "­",  # Soft Hyphen
    "͏",  # Combining Grapheme Joiner
}

def clean_text(text: str) -> str:
    """移除零寬字元、統一全形、替換混淆詞"""
    # 移除所有零寬字元（明確清單 + Cf 類）
    text = "".join(
        c for c in text
        if c not in _ZERO_WIDTH and unicodedata.category(c) != "Cf"
    )
    # NFKC 正規化（全形→半形）
    text = unicodedata.normalize("NFKC", text)
    # 移除字符間的符號分隔混淆（如 广+告+代+发、广·告·代·发）
    import re as _re
    text = _re.sub(r'(?<=[一-鿿])[+·•/\\\-\.。，,\s]+(?=[一-鿿])', '', text)
    # 混淆詞替換
    for k, v in _CONFUSE_MAP.items():
        text = text.replace(k, v)
    return text.strip()


